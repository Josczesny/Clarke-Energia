// Função para gerar fornecedores para cada estado
export const gerarFornecedoresPorEstado = () => {
  const estados = [
    'AC', 'AL', 'AP', 'AM', 'BA', 'CE', 'DF', 'ES', 'GO',
    'MA', 'MT', 'MS', 'MG', 'PA', 'PB', 'PR', 'PE', 'PI',
    'RJ', 'RN', 'RS', 'RO', 'RR', 'SC', 'SP', 'SE', 'TO'
  ];

  const fornecedoresPorEstado = {};

  estados.forEach(estado => {
    fornecedoresPorEstado[estado] = fornecedores.map(f => ({
      ...f,
      id: `${f.id}-${estado}`,
      estadoOrigem: estado,
      custoKwh: f.custoKwh + Math.random() * 0.1 - 0.05,
      limiteMinKwh: f.limiteMinKwh,
      limiteMaxKwh: f.limiteMaxKwh
    }));
  });

  return fornecedoresPorEstado;
}; 